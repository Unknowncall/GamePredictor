
public class Game {
	int bSide;
	int rSide;
	int gID;
	int result;
	
	
	public Game(int bSide, int rSide, int gID, int result) {
		super();
		this.bSide = bSide;
		this.rSide = rSide;
		this.gID = gID;
		this.result = result;
	}
	public int getbSide() {
		return bSide;
	}
	public void setbSide(int bSide) {
		this.bSide = bSide;
	}
	public int getrSide() {
		return rSide;
	}
	public void setrSide(int rSide) {
		this.rSide = rSide;
	}
	public int getgID() {
		return gID;
	}
	public void setgID(int gID) {
		this.gID = gID;
	}
	public int getResult() {
		return result;
	}
	public void setResult(int result) {
		this.result = result;
	}
	
	
	
}
